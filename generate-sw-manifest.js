#!/usr/bin/env node
/**
 * Walks dist/ and writes dist/sw-manifest.js exporting CACHE_FILES — the array
 * the hand-written service worker pre-caches on install.
 *
 * Strict rules:
 *   - Static shell assets ONLY (html, css, js, json, icons, fonts, vendored libs).
 *   - NEVER include user data; IndexedDB is the source of truth for that.
 *   - Skip sw.js and sw-manifest.js themselves (the SW registers separately).
 */

const fs = require('fs');
const path = require('path');
const crypto = require('crypto');

const DIST = path.join(__dirname, 'dist');

const SKIP_FILES = new Set(['sw.js', 'sw-manifest.js']);
const ALLOWED_EXT = new Set([
  '.html',
  '.css',
  '.js',
  '.json',
  '.svg',
  '.png',
  '.jpg',
  '.jpeg',
  '.webp',
  '.woff',
  '.woff2',
  '.ttf',
  '.ico',
  '.map',
]);

function walk(dir, base = '') {
  const out = [];
  if (!fs.existsSync(dir)) return out;
  for (const entry of fs.readdirSync(dir, { withFileTypes: true })) {
    const rel = base ? `${base}/${entry.name}` : entry.name;
    const full = path.join(dir, entry.name);
    if (entry.isDirectory()) {
      out.push(...walk(full, rel));
    } else {
      if (SKIP_FILES.has(rel)) continue;
      const ext = path.extname(entry.name).toLowerCase();
      if (!ALLOWED_EXT.has(ext)) continue;
      if (ext === '.map') continue;
      out.push(`./${rel}`);
    }
  }
  return out;
}

function main() {
  if (!fs.existsSync(DIST)) {
    console.error('[sw-manifest] dist/ does not exist — run build first');
    process.exit(1);
  }
  const files = walk(DIST).sort();
  if (!files.includes('./index.html') && fs.existsSync(path.join(DIST, 'index.html'))) {
    files.unshift('./index.html');
  }
  const contentHasher = crypto.createHash('sha256');
  for (const rel of files) {
    const abs = path.join(DIST, rel.replace(/^\.\//, ''));
    contentHasher.update(rel);
    if (fs.existsSync(abs)) contentHasher.update(fs.readFileSync(abs));
  }
  const cacheKey = contentHasher.digest('hex').slice(0, 10);
  const cacheName = `macadam-shell-${cacheKey}`;
  const banner = `/* AUTO-GENERATED by generate-sw-manifest.js — do not edit by hand. */`;
  const body =
    `export const CACHE_NAME = ${JSON.stringify(cacheName)};\n` +
    `export const CACHE_FILES = ${JSON.stringify(files, null, 2)};\n`;
  fs.writeFileSync(path.join(DIST, 'sw-manifest.js'), `${banner}\n${body}`);
  console.log(`[sw-manifest] wrote ${files.length} entries to dist/sw-manifest.js`);
}

main();
